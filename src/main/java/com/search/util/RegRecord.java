package com.search.util;

public class RegRecord {


    private String registration, make, model, color, year;


    public RegRecord(String registration, String make, String model, String color, String year) {
        this.registration = registration;
        this.make = make;
        this.model = model;
        this.color = color;
        this.year = year;


    }

    public String getRegistration() {
        return registration;
    }


    public String getMake() {
        return make;
    }


    public String getModel() {
        return model;
    }


    public String getYear() {
        return year;
    }

    public String getColor() {
        return year;
    }


}
